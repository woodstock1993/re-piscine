#include <unistd.h>

int	ft_strcmp(char *a, char *b)
{
	int	i;

	i = 0;
	while (a[i] || b[i])
	{
		if (a[i] == b[i])
		{
			a++;
			b++;
		}
		else
			break;
	}
	return (a[i] - b[i]);
}
